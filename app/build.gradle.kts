fun loadEnvFile(path: String): Map<String, String> {
    val file = File(path)
    if (!file.exists()) return emptyMap()

    return file.readLines()
        .filter { it.isNotBlank() && !it.trim().startsWith("#") && it.contains("=") }
        .associate {
            val (key, value) = it.split("=", limit = 2)
            key.trim() to value.trim()
        }
}

